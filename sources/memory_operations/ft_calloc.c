#include "../../includes/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*mem;

	mem = malloc(count * size);
	if (!mem)
		return (0);
	mem = ft_bzero(mem, count * size);
	return (mem);
}
